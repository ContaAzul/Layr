package layr.sample;

public class World {
	private String name;
	private Hello hello;
	private Long id;

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public Hello getHello() {
		return hello;
	}

	public void setHello(Hello hello) {
		this.hello = hello;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}
	
}